def get_rules_for_drug(drug_name: str) -> List[FoodRule]:
    """
    根据药物名称（通用名或商品名）匹配所有适用的饮食规则。
    匹配策略：关键词子串匹配（大小写不敏感）。
    同一药物可能匹配多条规则（如华法林匹配维生素K规则和葡萄柚规则）。
    """
    if not drug_name:
        return []
    name_lower = drug_name.lower().strip()
    matched = []
    for rule in DRUG_FOOD_RULES:
        for kw in rule.drug_keywords:
            if kw.lower() in name_lower or name_lower in kw.lower():
                matched.append(rule)
                break  # 同一规则只匹配一次
    return matched
